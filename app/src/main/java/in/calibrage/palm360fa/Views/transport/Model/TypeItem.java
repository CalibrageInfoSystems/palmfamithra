package in.calibrage.palm360fa.Views.transport.Model;

public class TypeItem {
    int Id;
    String name;

    public TypeItem(int id, String name) {
        Id = id;
        this.name = name;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
