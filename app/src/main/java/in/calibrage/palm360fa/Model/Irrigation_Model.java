package in.calibrage.palm360fa.Model;

public class Irrigation_Model {
    private String plotCode,name,updatedBy,updatedbyDate ;


    public Irrigation_Model() {
        this.plotCode = plotCode;
        this.name = name;
        this.updatedBy = updatedBy;
        this.updatedbyDate = updatedbyDate;
    }

    public String getPlotCode() {
        return plotCode;
    }

    public void setPlotCode(String plotCode) {
        this.plotCode = plotCode;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

    public String getUpdatedbyDate() {
        return updatedbyDate;
    }

    public void setUpdatedbyDate(String updatedbyDate) {
        this.updatedbyDate = updatedbyDate;
    }
}
